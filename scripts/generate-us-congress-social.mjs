import fs from "node:fs/promises";
import { parse } from "yaml";

const sourceUrl = "https://raw.githubusercontent.com/unitedstates/congress-legislators/main/legislators-social-media.yaml";
const response = await fetch(sourceUrl);

if (!response.ok) {
  throw new Error(`Unable to fetch congressional social media data: ${response.status} ${response.statusText}`);
}

const records = parse(await response.text()) ?? [];
const generatedAt = new Date().toISOString().slice(0, 10);
const byBioguide = {};

records.forEach((record) => {
  const bioguide = record?.id?.bioguide;
  if (!bioguide) return;
  const social = record.social ?? {};
  byBioguide[bioguide] = {
    id: {
      bioguide,
      govtrack: record.id?.govtrack ?? null,
      thomas: record.id?.thomas ?? null
    },
    social: {
      twitter: normalizeHandle(social.twitter),
      twitterId: social.twitter_id ? String(social.twitter_id) : null,
      facebook: social.facebook ?? null,
      youtube: social.youtube ?? null,
      youtubeId: social.youtube_id ?? null,
      instagram: social.instagram ?? null,
      instagramId: social.instagram_id ? String(social.instagram_id) : null
    }
  };
});

const twitterHandles = Object.values(byBioguide).filter((entry) => entry.social.twitter).length;
const moduleText = `// Generated by scripts/generate-us-congress-social.mjs from ${sourceUrl}
// Generated on ${generatedAt}. Do not edit individual records by hand.

export const usCongressSocialMetadata = ${JSON.stringify({
  sourceUrl,
  generatedAt,
  total: Object.keys(byBioguide).length,
  twitterHandles
}, null, 2)};

export const usCongressSocialByBioguide = ${JSON.stringify(byBioguide, null, 2)};
`;

await fs.writeFile(new URL("../src/usCongressSocial.js", import.meta.url), moduleText);

console.log(`Generated ${twitterHandles} official congressional X handles from ${Object.keys(byBioguide).length} social records.`);

function normalizeHandle(value) {
  return `${value ?? ""}`.replace(/^@/, "").trim() || null;
}
