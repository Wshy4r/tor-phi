import { readFile, writeFile } from "node:fs/promises";
import crypto from "node:crypto";
import { turkishParliamentMembers, turkishParliamentMetadata } from "../src/turkishParliament.js";

const OUTPUT_FILE = new URL("../src/turkishParliament.js", import.meta.url);
const TRANSLATE_LIMIT = Number(process.env.TRANSLATE_LIMIT || "0");
const REQUEST_DELAY_MS = Number(process.env.TRANSLATE_DELAY_MS || "80");
const CHUNK_SIZE = Number(process.env.TRANSLATE_CHUNK_SIZE || "3200");

const sleep = (ms) => new Promise((resolve) => setTimeout(resolve, ms));

function hashText(text) {
  return crypto.createHash("sha1").update(text).digest("hex");
}

function chunkText(text) {
  const paragraphs = `${text ?? ""}`.split(/\n{2,}/);
  const chunks = [];
  let current = "";

  function pushCurrent() {
    if (current.trim()) chunks.push(current.trim());
    current = "";
  }

  for (const paragraph of paragraphs) {
    const clean = paragraph.trim();
    if (!clean) continue;

    if (clean.length > CHUNK_SIZE) {
      pushCurrent();
      const sentences = clean.match(/[^.!?。؟]+[.!?。؟]?\s*/g) ?? [clean];
      for (const sentence of sentences) {
        if ((current + sentence).length > CHUNK_SIZE) pushCurrent();
        if (sentence.length > CHUNK_SIZE) {
          for (let index = 0; index < sentence.length; index += CHUNK_SIZE) {
            chunks.push(sentence.slice(index, index + CHUNK_SIZE).trim());
          }
        } else {
          current += sentence;
        }
      }
      pushCurrent();
      continue;
    }

    if ((current + "\n\n" + clean).length > CHUNK_SIZE) pushCurrent();
    current = current ? `${current}\n\n${clean}` : clean;
  }

  pushCurrent();
  return chunks;
}

async function translateChunk(chunk, attempt = 1) {
  await sleep(REQUEST_DELAY_MS);
  const url = `https://translate.googleapis.com/translate_a/single?client=gtx&sl=tr&tl=en&dt=t&q=${encodeURIComponent(chunk)}`;
  const response = await fetch(url);

  if (!response.ok) {
    if (attempt < 5) {
      await sleep(750 * attempt);
      return translateChunk(chunk, attempt + 1);
    }
    throw new Error(`Translate HTTP ${response.status}`);
  }

  const data = await response.json();
  return (data?.[0] ?? []).map((part) => part?.[0] ?? "").join("");
}

async function translateText(text) {
  const translated = [];
  for (const chunk of chunkText(text)) translated.push(await translateChunk(chunk));
  return translated.join("\n\n").replace(/\n{3,}/g, "\n\n").trim();
}

function normalizeBiographyTranslation(text) {
  return `${text ?? ""}`
    .replace(/\bAK PARTY\b/g, "AK Party")
    .replace(/\bGood Party\b/g, "Good Party")
    .replace(/\bGrand National Assembly of Turkey\b/g, "Grand National Assembly of Turkiye")
    .replace(/\bTurkish Grand National Assembly\b/g, "Grand National Assembly of Turkiye")
    .replace(/\bTGNA\b/g, "TBMM")
    .replace(/\s+\n/g, "\n")
    .trim();
}

async function main() {
  const existingSource = await readFile(OUTPUT_FILE, "utf8");
  const sourceHeader = existingSource.split("export const turkishParliamentMetadata")[0] || "// Generated by scripts/generate-turkish-parliament.mjs from official TBMM pages.\n";
  const updatedMembers = [];
  let translated = 0;
  let skipped = 0;
  let errors = 0;

  for (const member of turkishParliamentMembers) {
    const biography = member.biography?.trim() || "";
    const biographyHash = biography ? hashText(biography) : "";
    const hasFreshTranslation = biography && member.englishBiography && member.biographyTranslationHash === biographyHash;

    if (!biography || hasFreshTranslation || (TRANSLATE_LIMIT > 0 && translated >= TRANSLATE_LIMIT)) {
      updatedMembers.push(member);
      skipped += 1;
      continue;
    }

    try {
      const englishBiography = normalizeBiographyTranslation(await translateText(biography));
      updatedMembers.push({
        ...member,
        englishBiography,
        biographyTranslationStatus: "translated-google-public",
        biographyTranslationHash: biographyHash
      });
      translated += 1;
      if (translated % 25 === 0) console.log(`Translated ${translated} biographies`);
    } catch (error) {
      updatedMembers.push({
        ...member,
        biographyTranslationStatus: "translation-error",
        biographyTranslationError: error.message,
        biographyTranslationHash: biographyHash
      });
      errors += 1;
    }
  }

  const source = `${sourceHeader}export const turkishParliamentMetadata = ${JSON.stringify(turkishParliamentMetadata, null, 2)};\n\nexport const turkishParliamentMembers = ${JSON.stringify(updatedMembers, null, 2)};\n`;
  await writeFile(OUTPUT_FILE, source);

  console.log(JSON.stringify({
    members: updatedMembers.length,
    translated,
    skipped,
    errors
  }, null, 2));
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
