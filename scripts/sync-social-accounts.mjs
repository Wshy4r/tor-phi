import fs from "node:fs/promises";
import { allSocialAccounts } from "../src/socialAccounts.js";
import { franceParliamentMembers } from "../src/franceParliament.js";
import { iranParliamentMembers } from "../src/iranParliament.js";
import { turkishParliamentMembers } from "../src/turkishParliament.js";
import { ukParliamentMembers } from "../src/ukParliament.js";
import { usCongressMembers } from "../src/usCongress.js";

const generatedAt = new Date().toISOString();
const outJson = new URL("../public/source/social/accounts.json", import.meta.url);
const outJs = new URL("../src/socialAccountRegistry.js", import.meta.url);

const accounts = allSocialAccounts
  .map((account) => ({
    countryId: account.countryId,
    ownerName: account.ownerName,
    ownerType: account.ownerType,
    role: account.role ?? null,
    handle: account.handle,
    url: account.url,
    profileHref: account.profileHref ?? null,
    label: account.label ?? "X"
  }))
  .sort((a, b) =>
    a.countryId.localeCompare(b.countryId) ||
    a.ownerType.localeCompare(b.ownerType) ||
    a.ownerName.localeCompare(b.ownerName) ||
    a.handle.localeCompare(b.handle)
  );

const countries = {};
const ownerTypes = {};

accounts.forEach((account) => {
  countries[account.countryId] = (countries[account.countryId] ?? 0) + 1;
  ownerTypes[account.ownerType] = (ownerTypes[account.ownerType] ?? 0) + 1;
});

const registry = {
  generatedAt,
  source: "TOR Phi internal profile graph",
  method: "Generated from profiles, parliaments, foreign ministries, think tanks, media authors, and official congressional social media data.",
  totals: {
    accounts: accounts.length,
    countries: Object.keys(countries).length
  },
  countries,
  ownerTypes,
  parliamentCoverage: buildParliamentCoverage(accounts),
  accounts
};

await fs.mkdir(new URL("../public/source/social/", import.meta.url), { recursive: true });
await fs.writeFile(outJson, JSON.stringify(registry, null, 2) + "\n");
await fs.writeFile(
  outJs,
  `// Generated by scripts/sync-social-accounts.mjs. Do not edit by hand.\nexport const socialAccountRegistry = ${JSON.stringify(registry, null, 2)};\n`
);

console.log(`Synced ${accounts.length} TOR Phi X accounts across ${Object.keys(countries).length} countries.`);
Object.entries(countries)
  .sort((a, b) => a[0].localeCompare(b[0]))
  .forEach(([countryId, count]) => console.log(`- ${countryId}: ${count}`));

function buildParliamentCoverage(accounts) {
  const parliamentSources = {
    usa: {
      label: "U.S. Congress",
      members: usCongressMembers,
      accountTypes: ["Congress"],
      source:
        "Official legislative-role social handles from unitedstates/congress-legislators legislators-social-media.yaml, keyed to Bioguide IDs.",
      sourceUrl: "https://raw.githubusercontent.com/unitedstates/congress-legislators/main/legislators-social-media.yaml"
    },
    france: {
      label: "National Assembly",
      members: franceParliamentMembers,
      accountTypes: ["Parliament"],
      source:
        "Official Assemblee nationale active-deputies open-data package contact fields, filtered to X/Twitter URLs.",
      sourceUrl: "https://data.assemblee-nationale.fr/acteurs/deputes-en-exercice"
    },
    uk: {
      label: "House of Commons",
      members: ukParliamentMembers,
      accountTypes: ["Parliament"],
      source:
        "Official UK Parliament Members API contact endpoint, filtered to X/Twitter URLs.",
      sourceUrl: "https://members-api.parliament.uk/index.html"
    },
    turkey: {
      label: "Grand National Assembly of Turkiye",
      members: turkishParliamentMembers,
      accountTypes: ["Parliament"],
      source:
        "TBMM member profile and contact pages are parsed; current imported pages expose email, phone, address, and an empty social container, but no per-member X handles.",
      sourceUrl: "https://www.tbmm.gov.tr/milletvekili/liste"
    },
    iran: {
      label: "Islamic Consultative Assembly / Majlis",
      members: iranParliamentMembers,
      accountTypes: ["Parliament"],
      source:
        "The current Majlis roster import does not expose verified member X handles. Social feed slots remain empty until official or individually verified handles are added.",
      sourceUrl: "https://en.parliran.ir/"
    }
  };

  return Object.fromEntries(Object.entries(parliamentSources).map(([countryId, config]) => {
    const accountTypes = new Set(config.accountTypes);
    const socialAccounts = accounts.filter((account) =>
      account.countryId === countryId && accountTypes.has(account.ownerType)
    );
    const membersWithSocial = new Set(socialAccounts.map((account) => account.ownerName));
    const missingMembers = config.members
      .filter((member) => !membersWithSocial.has(member.name))
      .slice(0, 80)
      .map((member) => ({
        id: member.id,
        name: member.name,
        party: member.party?.shortLabel || member.party || member.group?.shortLabel || member.faction || "",
        constituency: member.districtLabel || member.constituency?.label || member.constituency || member.province || "",
        profileHref: getParliamentProfileHref(countryId, member)
      }));
    const memberCount = config.members.length;
    const membersWithSocialCount = membersWithSocial.size;

    return [countryId, {
      countryId,
      label: config.label,
      memberCount,
      membersWithSocial: membersWithSocialCount,
      missingMembers: Math.max(memberCount - membersWithSocialCount, 0),
      accountCount: socialAccounts.length,
      coveragePercent: memberCount ? Math.round((membersWithSocialCount / memberCount) * 1000) / 10 : 0,
      source: config.source,
      sourceUrl: config.sourceUrl,
      missingSample: missingMembers
    }];
  }));
}

function getParliamentProfileHref(countryId, member) {
  if (countryId === "usa") return `/country/usa/congress/${encodeURIComponent(slugify(member.name))}`;
  return `/country/${countryId}/parliament/${encodeURIComponent(member.slug || slugify(member.name))}`;
}

function slugify(value) {
  return `${value ?? ""}`
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/ı/g, "i")
    .replace(/İ/g, "i")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}
